    public void buscar()
    {
        conexion conexion1;
        conexion1 = new conexion();
        try
        {
            Connection Reconectar;//conector
            Reconectar=conexion1.conectar();
            PreparedStatement pps;
            ResultSet rs;
            pps = Reconectar.prepareStatement("select * from tblautomoviles where ID = ?");
            pps.setString(1,this.jtxtIdProducto.getText().trim());
            
            rs = pps.executeQuery();
            
            if(rs.next())
            {
                this.jtxtIdProducto.setText(rs.getString("Id"));
                this.jtxtPlaca.setText(rs.getString("Placa"));
                this.jtxtModelo.setText(rs.getString("Modelo"));
                this.jtxtMarca.setText(rs.getString("Marca"));
                this.jtxtCilindraje.setText(rs.getString("Cilindraje"));
                this.jtxtccPropietario.setText(rs.getString("IDP"));
                this.jtxtNombre.setText(rs.getString("NombreP"));
                this.jtxtTelefono.setText(rs.getString("Telefono"));
                this.jDateChooser1.setDate(rs.getDate("Fecha"));
                
            }    
        }catch(SQLException e){
            
        }
    }

