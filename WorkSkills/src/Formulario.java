    public void insertar()
    {
        conexion conexion1; //variable conexion
        conexion1  = new conexion();//conexion
        try
        {   
            //java.util.Date date = (java.util.Date)jDateChooser1.getDate();
            Date date = (Date) jDateChooser1.getDate();
            long d1 = date.getTime();
            java.sql.Date Fecha = new java.sql.Date(d1);
            
            Connection Reconectar;//conector
            Reconectar=conexion1.conectar();//llamar la funcion conectar
            PreparedStatement pps;
            String sql;
            
            sql = "INSERT INTO tblautomoviles (ID,Placa,Modelo,Marca,Cilindraje,IDP,NombreP,Telefono,FechaIngreso,Estado) VALUES (?,?,?,?,?,?,?,?,?,?) ";
            pps=Reconectar.prepareStatement(sql);
            pps.setString(1,jtxtIdProducto.getText().trim());
            pps.setString(2,jtxtPlaca.getText().trim());
            pps.setString(3,jtxtModelo.getText().trim());
            pps.setString(4,jtxtMarca.getText().trim());
            pps.setString(5,jtxtCilindraje.getText().trim());
            pps.setString(6,jtxtccPropietario.getText().trim());
            pps.setString(7,jtxtNombre.getText().trim());
            pps.setString(8,jtxtTelefono.getText().trim());
            pps.setDate(9, Fecha);
            pps.setString(10,jCboxEstado.getSelectedItem().toString());
            pps.execute();
            pps.close();
            
            JOptionPane.showMessageDialog(null,"guardado con exito");
        }
        catch(SQLException e)
        {
           JOptionPane.showMessageDialog(null, e);
        }
        
    }
